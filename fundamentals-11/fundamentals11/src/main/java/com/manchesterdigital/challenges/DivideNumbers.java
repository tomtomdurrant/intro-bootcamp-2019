package com.manchesterdigital.challenges;

public class DivideNumbers {
    private final Double a;
    private final Double b;

    public DivideNumbers(Double a, Double b) {
        this.a = a;
        this.b = b;
    }

    public Double getA() {
        return a;
    }

    public Double getB() {
        return b;
    }
}
