package com.manchesterdigital;

import org.w3c.dom.ls.LSOutput;

public class Operators {
    public static void main(String[] args) {

//        Example of addition operator
        int result = 1 + 2;
        System.out.println("Result is " + result);

//    subtract
        result = 3 - 1;
        System.out.println("Result is " + result);

//        multiplication
        result = 5 * 9;
        System.out.println("Result is " + result);

//        Division
        result = 10 / 5;
        System.out.println("Result is " + result);

//        Modulus
        result = 20 % 10;
        System.out.println("Result is " + result);

//        Increment
        result = result + 1;
        System.out.println("Result is " + result);

//        Abbreviated
        result++;
        System.out.println("Result is " + result);
    }
}
